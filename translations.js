// Current language
let currentLanguage = 'en';

// Function to change language
function changeLanguage(lang) {
    currentLanguage = lang;
    updateContent();
    updateLanguageButton();
    updateLanguageDropdown();
    
    // Save language preference
    localStorage.setItem('preferredLanguage', currentLanguage);
}

// Function to update language button with current flag
function updateLanguageButton() {
    const button = document.getElementById('language-button');
    if (button) {
        const flags = {
            'en': 'ğŸ‡ºğŸ‡¸',
            'de': 'ğŸ‡©ğŸ‡ª',
            'tr': 'ğŸ‡¹ğŸ‡·'
        };
        button.innerHTML = flags[currentLanguage] || 'ğŸ‡ºğŸ‡¸';
    }
}

// Function to update language dropdown
function updateLanguageDropdown() {
    const dropdown = document.getElementById('language-dropdown');
    if (dropdown) {
        dropdown.style.display = 'none';
    }
}

// Function to toggle dropdown
function toggleDropdown() {
    const dropdown = document.getElementById('language-dropdown');
    if (dropdown) {
        dropdown.style.display = dropdown.style.display === 'none' ? 'block' : 'none';
    }
}

// Function to update content based on selected language
function updateContent() {
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLanguage][key]) {
            element.innerHTML = translations[currentLanguage][key];
        }
    });
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    initializeLanguage();
});

// Also run on window load to ensure it works
window.onload = function() {
    initializeLanguage();
};

// Function to initialize language
function initializeLanguage() {
    // Check if user has a saved preference
    const savedLanguage = localStorage.getItem('preferredLanguage');
    if (savedLanguage) {
        currentLanguage = savedLanguage;
    }
    
    updateContent();
    updateLanguageButton();
}
